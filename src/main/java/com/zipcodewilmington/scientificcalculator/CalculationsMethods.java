package com.zipcodewilmington.scientificcalculator;
//Munir
public class CalculationsMethods {

    public static void CalculationsMethods()  {   }

    public  Double multiplyNumbers(Double num1, Double num2){
        return (num1 * num2);
    }
    public  Double divideNumbers(Double num1, Double num2) {
        return (num1 / num2);
    }
    public  Double Modulus(Double num1, Double num2) {
        return (num1 % num2);
    }
    public  Double exponentv(Double num1, Double num2) {
        return (Math.pow(num1,num2));
    }
    public  Double squareroot(Double num1) {
        return Math.sqrt(num1);
    }
    public  Double squared(Double num1) {
        return Math.pow(num1,2);
    }
    public  Double Sin(Double num1) {
        return Math.sin(num1);
    }
    public  Double Cos(Double num1) {
        return Math.cos(num1);
    }
    public  Double Tan(Double num1) {
        return Math.tan(num1);
    }
}